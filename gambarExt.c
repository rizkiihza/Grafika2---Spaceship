int isValid(char* test,int len){
	int i;
	int count = 0;
	for(i=0;i<len;i++){
		if(test[i] == ' '){
			count = 0;
		}
		else{
			count++;
		}
		
		if(count>30){
			return 0;
		}
	}
	return 1;
}

int findSpace(char* test,int len, int start){
	int x = start + 29;
	if (x >= len){
		return len;
	}
	else{
		while(test[x] != ' '){
			x--;
		}
		return x;
	}
}


